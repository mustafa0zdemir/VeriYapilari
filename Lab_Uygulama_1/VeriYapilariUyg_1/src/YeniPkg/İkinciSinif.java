package YeniPkg;

public class İkinciSinif {
	private String ogretmen;
	private String brans;
	private int akademikYayinSayisi;
	
	
	//get ve set metotları
	public void setOgretmen(String ogretmen) {
		this.ogretmen = ogretmen;
		
	}
	public void setBrans(String brans) {
		this.brans = brans;
	}
	
	public void setAkademikYayin(int akademikYayinSayisi) {
		this.akademikYayinSayisi = akademikYayinSayisi;
		
	}
	
	public String getOgretmen() {
		return ogretmen;
		
	}
	
	public String getBrans() {
		return brans;
		
	}
	public int getAkademikYayinSayisi() {
		return akademikYayinSayisi;
	
	}	
	



}

